

// 滑鼠事件
                        // 使用時機
onclick                 // 在元素終點及滑鼠左鍵
ondblclick              // 在元素中霜及滑鼠左鍵
onmousedown             // 在元素中按下任一滑鼠按鈕
onmouseover             // 當滑鼠移入至元素時
onmouseout              // 當滑鼠從元素移出時

// 鍵盤事件

onkeydown				// 正按下的鍵盤按鍵
onkeypress				// 鍵盤任一按鍵被按下
onkeyup					// 放開鍵盤任一按鍵

// 視窗事件

onerror					// 載入的檔案或圖片等發生問題
onload					// 當頁面載入完成
onresize				// 當視窗大小改變
onscroll				// 當滾動頁面或移動卷軸

// 表單事件

onselect				// 當選取區塊內文字
onchange				// 當改變元素內容
onblur					// 當滑鼠從特定區塊離開
onfocus					// 當滑鼠進入特定區塊
onsubmit				// 當點擊按鈕提交表單